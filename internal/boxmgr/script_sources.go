func parseScriptNodes(stdout string, maxNodes int) ([]config.NodeConfig, error) {
	text := strings.TrimSpace(stdout)
	if text == "" {
		return nil, errors.New("脚本输出为空（stdout 需要输出 JSON）")
	}

	if maxNodes <= 0 {
		maxNodes = 2000
	}

	var raw any
	dec := json.NewDecoder(strings.NewReader(text))
	dec.UseNumber()
	if err := dec.Decode(&raw); err != nil {
		return nil, fmt.Errorf("解析脚本输出 JSON 失败: %w", err)
	}

	var rawNodes []any
	switch v := raw.(type) {
	case []any:
		rawNodes = v
	case map[string]any:
		if nodesVal, ok := v["nodes"]; ok {
			if arr, ok := nodesVal.([]any); ok {
				rawNodes = arr
			} else {
				return nil, errors.New("脚本输出字段 nodes 必须是数组")
			}
		} else if nodeVal, ok := v["node"]; ok {
			rawNodes = []any{nodeVal}
		} else {
			return nil, errors.New("脚本输出必须包含 nodes 数组")
		}
	default:
		return nil, errors.New("脚本输出 JSON 顶层必须是对象或数组")
	}

	if len(rawNodes) > maxNodes {
		return nil, fmt.Errorf("节点数量过多: %d (max %d)", len(rawNodes), maxNodes)
	}

	out := make([]config.NodeConfig, 0, len(rawNodes))
	seen := make(map[string]struct{}, len(rawNodes))
	for idx, item := range rawNodes {
		if item == nil {
			continue
		}
		switch v := item.(type) {
		case string:
			uri := strings.TrimSpace(v)
			if uri == "" {
				continue
			}
			if _, ok := seen[uri]; ok {
				continue
			}
			seen[uri] = struct{}{}
			out = append(out, config.NodeConfig{URI: uri})
		case map[string]any:
			// Re-marshal to leverage struct tags and keep behavior stable.
			b, _ := json.Marshal(v)
			var obj scriptNodeObject
			if err := json.Unmarshal(b, &obj); err != nil {
				return nil, fmt.Errorf("解析节点对象失败 (index %d): %w", idx, err)
			}
			uri := strings.TrimSpace(obj.URI)
			if uri == "" {
				uri = strings.TrimSpace(obj.URL)
			}
			if uri == "" {
				return nil, fmt.Errorf("节点缺少 uri 字段 (index %d)", idx)
			}
			if _, ok := seen[uri]; ok {
				continue
			}
			seen[uri] = struct{}{}
			out = append(out, config.NodeConfig{
				Name:    strings.TrimSpace(obj.Name),
				URI:     uri,
				Region:  strings.TrimSpace(obj.Region),
				Country: strings.TrimSpace(obj.Country),
			})
		default:
			return nil, fmt.Errorf("不支持的节点类型 (index %d)", idx)
		}
	}

	if len(out) == 0 {
		return nil, errors.New("脚本未返回任何节点")
	}
	return out, nil
}

